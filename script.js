// Elementos do modal
const modal = document.getElementById('articleModal');
const modalImage = document.getElementById('modalImage');
const modalBadge = document.getElementById('modalBadge');
const modalTitle = document.getElementById('modalTitle');
const modalSubtitle = document.getElementById('modalSubtitle');
const modalContent = document.getElementById('modalContent');
const closeBtn = document.querySelector('.modal-close');

// Função para abrir o modal
function openModal(articleId) {
    const article = articles[articleId];

    if (article) {
        // Preencher o modal com os dados do artigo
        modalImage.src = article.image;
        modalImage.alt = article.title;
        modalBadge.textContent = article.category;
        modalTitle.textContent = article.title;
        modalSubtitle.textContent = article.subtitle;

        // Limpar e preencher o conteúdo
        modalContent.innerHTML = '';
        article.content.forEach(paragraph => {
            const p = document.createElement('p');
            p.textContent = paragraph;
            modalContent.appendChild(p);
        }
        );

        // Mostrar o modal
        modal.classList.add('show');
        document.body.style.overflow = 'hidden';
    }
}

// Função para fechar o modal
function closeModal() {
    modal.classList.remove('show');
    document.body.style.overflow = 'auto';
}

// Adicionar event listeners aos cards
document.querySelectorAll('[data-article]').forEach(card => {
    card.addEventListener('click', function() {
        const articleId = this.getAttribute('data-article');
        openModal(articleId);
    });
}
);

// Fechar modal ao clicar no botão X
closeBtn.addEventListener('click', closeModal);

// Fechar modal ao clicar fora do conteúdo
modal.addEventListener('click', function(e) {
    if (e.target === modal) {
        closeModal();
    }
});

// Fechar modal com a tecla ESC
document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape' && modal.classList.contains('show')) {
        closeModal();
    }
});

// Animação suave ao rolar a página
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function(e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
}
);
